/*
 * Generate an AES key schedule
 */
void KeyExpansion(const uint8_t *key, uint32_t *roundKey)
{	
	uint8_t *g, tmp;
    
    // w[0]~w[3] 키 값 채우기
    for(int i=0;i<Nk;i++) memcpy(roundKey+i, key+4*i, sizeof(uint8_t)*4);

    // w[4]~w[43] 키 값 채우기
	for(int i=Nk;i<RNDKEYLEN;i++){
        if(i%Nk==0){    // w[4], w[8].. 등 g(w)와의 연산이 필요할 때
            memcpy(roundKey+i, roundKey+i-1, sizeof(uint32_t));
            g = (uint8_t *)&roundKey[i];

            // LRotWord와 S-Box 연산
            tmp=sbox[g[0]]; g[0]=sbox[g[1]]; g[1]=sbox[g[2]]; g[2]=sbox[g[3]], g[3]=tmp;

            // Rcon과 XOR 연산
            roundKey[i] = roundKey[i] ^ Rcon[i/Nk] ^ roundKey[i-4];
        }else{ 
            // 직전 w값과 XOR 연산
            roundKey[i] = roundKey[i-1]^roundKey[i-4];
        }
	}
}

void AddRoundKey(uint8_t *state, const uint32_t *roundKey){
    uint8_t *p;

    // state와 현재 라운드 키를 XOR 연산
    for(int i=0;i<BLOCKLEN;i++){
        p = (uint8_t *)&roundKey[i/4];  // 연산을 위해 uint8_t *로 형변환
        state[i] ^= p[i%4];
    }
}

void SubBytes(uint8_t *state, int mode){
    const uint8_t *p;

    // 현재 모드에 따라 sbox 선택
    if(mode==ENCRYPT) p=sbox;
    else p=isbox;
    
    // 선택된 sbox에 따라 byte를 대체
    for(int i=0;i<BLOCKLEN;i++) state[i] = p[state[i]];
}

void ShiftRows(uint8_t *state, int mode){
    uint8_t tmp;

    if(mode==ENCRYPT){  // 암호화 모드일 때
        // 규칙에 따라 shift
        tmp=state[1]; state[1]=state[5]; state[5]=state[9]; state[9]=state[13]; state[13]=tmp;
        tmp=state[2]; state[2]=state[10]; state[10]=tmp; tmp=state[6]; state[6]=state[14]; state[14]=tmp;
        tmp=state[15]; state[15]=state[11]; state[11]=state[7]; state[7]=state[3]; state[3]=tmp;
    }else{  // 복호화 모드일 때
        // 규칙에 따라 shift
        tmp=state[13]; state[13]=state[9]; state[9]=state[5]; state[5]=state[1]; state[1]=tmp;
        tmp=state[2]; state[2]=state[10]; state[10]=tmp; tmp=state[6]; state[6]=state[14]; state[14]=tmp;
        tmp=state[3]; state[3]=state[7]; state[7]=state[11]; state[11]=state[15]; state[15]=tmp;
    }
    
}

void MixColumns(uint8_t *state, int mode){
    const uint8_t *p;
    uint8_t tmp[BLOCKLEN]={0,}, m=1<<4|1<<3|1<<1|1; // m(x) = x^8+x^4+x^3+x+1

    // 모드에 따라 매트릭스 선택
    if(mode==ENCRYPT) p = M;
    else p = IM;

    for(int i=0;i<BLOCKLEN;i++){
        uint8_t a, b, res;
        
        // 행렬곱 연산
        for(int j=0;j<Nb;j++){
            a = state[(i/4)*4+j];
            b = p[(i%4)*4+j];
            res = 0;

            // 선택된 a와 b의 GF(2^8)에서의 곱셈
            while(b>0){ 
                if(b&1) res ^= a;    
                b = b>>1;   
                a = (a<<1) ^ ((a>>7)&1?m:0);  
            }

            // 곱셈 결과를 XOR 연산
            tmp[i] ^= res;
        }
    }

    // 연산한 state 값들을 갱신
    for(int i=0;i<BLOCKLEN;i++){
        state[i] = tmp[i];
    }
}

/*
 * AES cipher function
 * If mode is nonzero, then do encryption, otherwise do decryption.
 */
void Cipher(uint8_t *state, const uint32_t *roundKey, int mode)
{
    const uint32_t *kp;

    if(mode==ENCRYPT){  // 암호화 모드일 때
        // w[0]~w[3]와 Add Round Key 단계 수행
        kp = roundKey;
        AddRoundKey(state, kp);

        // 라운드 반복
        for(int i=0;i<Nr;i++){
            // Substitute Bytes 단계 수행
            SubBytes(state, mode);

            // Shift Rows 단계 수행
            ShiftRows(state, mode);

            // Mix Columns 단계 수행
            if(i!=Nr-1) MixColumns(state, mode);    // 마지막 라운드에서는 진행하지 않음

            // Add Round Key 단계 수행 
            kp = &roundKey[Nk*(i+1)];
            AddRoundKey(state, kp);
        }
    }else{  // 복호화 모드일 때
        // w[40]~w[43]와 Add Round Key 단계 수행
        kp = &roundKey[RNDKEYLEN-Nk];        
        AddRoundKey(state, kp);

        // 라운드 반복
        for(int i=0;i<Nr;i++){
            // Inverse Shift Rows 단계 수행
            ShiftRows(state, mode);

            // Inverse Subtitute Bytes 단계 수행
            SubBytes(state, mode);

            // Add Round Key 단계 수행 
            kp = &roundKey[RNDKEYLEN-Nk*(i+2)];
            AddRoundKey(state, kp);

            // Inverse Mix Columns 단계 수행      
            if(i!=Nr-1) MixColumns(state, mode);
        }
    }
    
}
